public class Compare {
    public static void main(String[] args) {
        int x = 21;
        int y = 100;
        System.out.println("x>y返回值为：" + (x > y));
        System.out.println("x<y返回值为：" + (x < y));
        System.out.println("x==y返回值为：" + (x == y));
        System.out.println("x!=y返回值为：" + (x != y));
        System.out.println("x>=y返回值为：" + (x >= y));
        System.out.println("x<=y返回值为：" + (x <= y));
    }
}
